package UnaryOperator;
public class UnaryOperator {
    public static void main(String[] args) {
            int x = 10;
            boolean benar = true;
        System.out.println("Pre - Increment : tamabahan duku baru di cetak");
        System.out.println("x awal : "+ x);
        System.out.println("++x : "+ ++x);
        System.out.println("setelah x : " + x);

        System.out.println("Pre - Increment : cetak dulu baru di tambahkan");
        System.out.println("x awal : " + x);
        System.out.println("++x : "+ ++x);
        System.out.println("setelah x : " + x);

        System.out.println("value variable !benar :"+ !benar);

    }
}
